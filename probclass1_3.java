public class probclass1_3{
    public probclass1_3(int a, int b){
        int i ;
        int j ;

        for(i = 0; i < a; i++){
            for(j = 0; j < b; j++){
                if(i <= 0 && j <= 0){
                    return 0;
                }else{
                   return a;
                }
            }
        }
    }
}